    void setComboCategory(){
        jComboCategory.removeAllItems();
        List<Category> list = vegetableBLL.getListCategory();
        if(list.size() ==0){
            jComboCategory.addItem("Empty");
            jComboCategory.setForeground(Color.red);
        }else{
            for(Category c : list){
                jComboCategory.addItem(c.toString());
                jComboCategory.setForeground(Color.black);
            }
        }
    }
        

