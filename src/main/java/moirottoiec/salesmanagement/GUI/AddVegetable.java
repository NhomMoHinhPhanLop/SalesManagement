    private void btnAddFrAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFrAddActionPerformed
        String name = textVegeName.getText();
        String unit = textUnit.getText();
        String amount = textAmount.getText();
        String img = textImg.getText();
        String price = textPrice.getText();
                
        if(name.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập name!");
        }
        else if(unit.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập unit!");
        }
        else if(amount.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập amount!");
        }
        else if(img.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập img link!");
        }
        
        else if(price.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập price!");
        }
        else{
            String str =(String) jComboCategory.getSelectedItem();
            String[] strsl = str.split(" ");
            int cateidV = Integer.parseInt(strsl[0]);
            String nameV = textVegeName.getText();
            String unitV = textUnit.getText();
            int amountV = Integer.parseInt(textAmount.getText());
            String imgV = "images/"+textImg.getText();
            float priceV = Float.parseFloat(textPrice.getText()) ;
            
            VegetableBLL veBLL = new VegetableBLL();
            veBLL.addVegetable( cateidV, nameV, unitV, amountV, imgV, priceV);
            JOptionPane.showMessageDialog(this, "Added success!");
            textVegeName.setText("");
            textUnit.setText("");
            textAmount.setText("");
            textImg.setText("");
            textPrice.setText("");
        }
        
        
        
        
    }//GEN-LAST:event_btnAddFrAddActionPerformed

