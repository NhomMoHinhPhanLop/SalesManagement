    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        VegetableBLL vegeBLL = new VegetableBLL();
        int rowSelect = VegetableTable.getSelectedRow();
        
        if (rowSelect == -1){
            JOptionPane.showMessageDialog(VegetableGUI.this, "Please choose a product !", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(VegetableGUI.this, "Are you sure??");
            if(confirm == JOptionPane.YES_OPTION){

                int vegeid =(int) VegetableTable.getValueAt(rowSelect, 1);
                int cateid = (int) VegetableTable.getValueAt(rowSelect, 2);
                String name = (String) VegetableTable.getValueAt(rowSelect, 3);
                String unit =(String) VegetableTable.getValueAt(rowSelect, 4);
                int amount = (int) VegetableTable.getValueAt(rowSelect, 5);
                String img = (String) VegetableTable.getValueAt(rowSelect, 6);
                float price = (float) VegetableTable.getValueAt(rowSelect, 7);
                vegeBLL.deleteVegetable(vegeid,cateid,name,unit,amount,img,price);
                
                
                
                getVegetableList();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


