    public void getVegetableList(){
        dtm = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        dtm.addColumn("STT");
        dtm.addColumn("VegetableID");
        dtm.addColumn("CategoryID");
        dtm.addColumn("VegetableName");
        dtm.addColumn("Unit");
        dtm.addColumn("Amount");
        dtm.addColumn("Images");
        dtm.addColumn("Price");
        
        VegetableTable.setModel(dtm);
        
        vegetableBLL = new VegetableBLL();
        int i = 1;
        for (Iterator iterator = vegetableBLL.getListVegetable().iterator(); iterator.hasNext();) {
            Vegetable vegetable = (Vegetable) iterator.next();
            Object[] row = {i++,vegetable.getVegetableID(),vegetable.getCategory().getCategoryID(),vegetable.getVegetableName(),vegetable.getUnit(),vegetable.getAmount(),vegetable.getImage(),vegetable.getPrice() };
            dtm.addRow(row);   
        } 
        
    }

